class Solution:

	def print2largest(self,arr, n):
	    maxi=-1;second_m=-1
	    for i in arr:
	        if i>maxi:
	            second_m=maxi
	            maxi=i
	        if i!=maxi and i>second_m:
	            second_m=i
	    return second_m if second_m!=maxi else -1